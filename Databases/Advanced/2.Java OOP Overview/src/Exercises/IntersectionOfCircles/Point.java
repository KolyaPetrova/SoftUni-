public class Point {
    Integer x;

    public Point(Integer x, Integer y) {
        this.x = x;
        this.y = y;
    }

    Integer y;

    public Integer getX() {
        return this.x;
    }

    public void setX(Integer x) {
        this.x = x;
    }

    public Integer getY() {
        return this.y;
    }

    public void setY(Integer y) {
        this.y = y;
    }

}
